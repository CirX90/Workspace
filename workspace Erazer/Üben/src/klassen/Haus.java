package Klassen;

public class Haus {
	
	// Variablen deklarieren
	int breite;
	int hoehe;
	int tiefe;
	
	String farbe;
	
	// Konstruktor
	public Haus(int breite, int hoehe, int tiefe, String farbe) {
		
		this.breite = breite;
		this.hoehe = hoehe;
		this.tiefe = tiefe;
		this.farbe = farbe;
		
	}

}