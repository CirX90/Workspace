package aufgabenblatt3_1;

public class Ausdruecke2 {
	public static void main(String[] args) {

		// Aussage a)

		// Berechnung des Umfangs
		double radius = 1.6;
		double kreisUmfang = (2 * Math.PI * radius);

		// Berechnung des Flächeninhalts
		double seitenlaenge = 0.25;
		double flaecheninhalt = seitenlaenge * seitenlaenge;

		// Auswertung der Aussage a)
		boolean ausdruckswertA = kreisUmfang > 1.5 && flaecheninhalt < 0.5;

		// Ausgabe auf der Konsole
		System.out.println("Die Aussage a) ist: " + ausdruckswertA);

		// Aussage b)

		boolean markiert = true;
		double zahl = 2.5;

		// Auswertung des Aussage b)
		boolean ausdruckswertB = markiert ^ (int) zahl != 15;

		// Ausgabe auf der Konsole
		System.out.println("Die Aussage b) ist: " + ausdruckswertB);

		// Aussage c)

		// Definitionen der Fließkommazahlen
		double ersteZahl = 4;
		double zweiteZahl = 4;

		// Auswertung des Aussage c)
		boolean ausdruckswertC = ersteZahl <= 1.5 && zweiteZahl >= 3.2
				&& ersteZahl == zweiteZahl;

		// Ausgabe auf der Konsole
		System.out.println("Die Aussage c) ist: " + ausdruckswertC);

	}
}